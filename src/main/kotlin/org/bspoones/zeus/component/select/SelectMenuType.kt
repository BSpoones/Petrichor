package org.bspoones.zeus.component.select

/**
 * Represents the type of select menu.
 *
 * This enum class defines the types of select menus supported:
 * - [STRING]: A select menu with string options.
 * - [ENTITY]: A select menu with entity options.
 *
 * @property STRING A select menu with string options.
 * @property ENTITY A select menu with entity options.
 * @author <a href="https://www.bspoones.com">BSpoones</a>
 */
enum class SelectMenuType {
    STRING,
    ENTITY;
}